CHUNK_SIZE = 0x3a
def decrypt_firmware(path, decrypted_path):
    with open(path, "rb") as src, open (decrypted_path, "wb") as decrypted:
        header = parse_header(src.read(0x20))
        while True:
            chunk = src.read(CHUNK_SIZE)
            if not chunk:
                break
            c = decrypt_chunk(header.encrypt_pos, chunk)
            decrypted.write(c)
    
